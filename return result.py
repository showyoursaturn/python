def f(n):
    a = [1, 1, 2, 3, 2, 1, 4, 1, 1, 1, 2, 4, 2, 3, 4]
    idx = 0

    while n < 15:
        n += a[n]
        idx += 1
    return idx
#리턴값이 5가 되는 경우
k = 0
for n in range (0,15):
    if f(n) == 5:
        print(n)
        k += 1

print(k)

